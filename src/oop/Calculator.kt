package src.oop

class Calculator {
	companion object {
		val intType = true

		// class functions!
		fun sum(a: Int, b: Int): Int {
			return a + b
		}

		fun subtract(a: Int, b: Int): Int {
			return a - b
		}

		fun multiply(a: Int, b: Int): Int {
			return a * b
		}

		fun divide(a: Int, b: Int): Int {
			return a / b
		}

	}

}
